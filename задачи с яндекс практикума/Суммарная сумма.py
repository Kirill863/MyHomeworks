"""Найти сумму цифр числа не так сложно, но что, если чисел несколько?
Напишите программу для выполнения этого действия.

Формат ввода
В первой строке указано число 
N
N Во всех последующих 
N
N строках написано по одному числу.

Формат вывода
Требуется вывести общую сумму цифр всех введённых чисел (кроме 
N
N)."""

N = int(input())
total_sum = 0

for i in range(N):
    number = input()
    # Преобразуем строку в число и суммируем его цифры
    digit_sum = sum(int(digit) for digit in number)
    total_sum += digit_sum

print(total_sum)
